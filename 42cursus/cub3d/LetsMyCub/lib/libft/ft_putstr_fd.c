#include "libft.h"

void	ft_putstr_fd(char *s, int fd)
{
	int		ps;

	ps = 0;
	if (!s)
		return ;
	while (s[ps] != '\0')
	{
		write(fd, &s[ps], 1);
		ps++;
	}
}
