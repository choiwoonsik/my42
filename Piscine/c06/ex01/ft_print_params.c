#include <unistd.h>

int	main(int argc, char *argv[])
{
	int		i;
	char	n;
	int		k;

	k = 1;
	while (k < argc)
	{
		i = 0;
		while (argv[k][i] != '\0')
		{
			n = argv[k][i];
			write(1, &n, 1);
			i++;
		}
		n = '\n';
		write(1, &n, 1);
		k++;
	}
}
