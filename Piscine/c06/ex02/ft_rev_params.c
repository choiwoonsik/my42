#include <unistd.h>

int	main(int argc, char *argv[])
{
	int		i;
	char	n;

	while (--argc > 0)
	{
		i = 0;
		while (argv[argc][i] != '\0')
		{
			n = argv[argc][i];
			write(1, &n, 1);
			i++;
		}
		n = '\n';
		write(1, &n, 1);
	}
}
