#include <unistd.h>

int		ft_str_is_lowercase(char *str)
{
	int		n;

	n = 0;
	while (*(str + n) != '\0')
	{
		if ((*(str + n) > 'z') || (*(str + n) < 'a'))
			return (0);
		n++;
	}
	return (1);
}
