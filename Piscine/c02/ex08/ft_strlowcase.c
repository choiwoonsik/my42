#include <unistd.h>

char	*ft_strlowcase(char *str)
{
	char	n;

	n = 0;
	while (*(str + n) != '\0')
	{
		if ((*(str + n) >= 'A') && (*(str + n) <= 'Z'))
			*(str + n) += 32;
		n++;
	}
	return (str);
}
