#include <unistd.h>

int		ft_str_is_alpha(char *str)
{
	int		n;

	n = 0;
	while (*(str + n) != '\0')
	{
		if (((*(str + n) <= 'Z') && (*(str + n) >= 'A'))
			|| ((*(str + n) <= 'z') && (*(str + n) >= 'a')))
		{
			n++;
			continue;
		}
		else
			return (0);
		n++;
	}
	return (1);
}
