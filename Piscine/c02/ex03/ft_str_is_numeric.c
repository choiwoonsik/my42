#include <unistd.h>

int		ft_str_is_numeric(char *str)
{
	int		n;

	n = 0;
	while (*(str + n) != '\0')
	{
		if ((*(str + n) > '9') || (*(str + n) < '0'))
			return (0);
		n++;
	}
	return (1);
}
