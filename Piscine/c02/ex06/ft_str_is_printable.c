#include <unistd.h>

int		ft_str_is_printable(char *str)
{
	int		n;

	n = 0;
	while (*(str + n) != '\0')
	{
		if ((*(str + n) <= 31) || (*(str + n) >= 127))
			return (0);
		n++;
	}
	return (1);
}
